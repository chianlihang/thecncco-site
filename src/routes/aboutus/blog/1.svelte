    <br>
    <p>  
        And that's a good segue into more about the future of the company. So where do I see this going? This is a tough one to answer being that we've yet to find product market fit. We hope the Kickstarter for Machinist will answer this question for us. But though the future is hazy, I have some personal opinions about this. The dogma of startups recently has been to "go with the flow". Simplified, this means being ready to pivot at any moment when a better opportunity arises. While on the micro level I find this helpful (ex: if a user doesn't like a website feature I came up with I'll change it), on the macro level I'd prefer to set a goal and gun for it (ex: I want to make CNCs for makers, so even if we don't find immediate success I want to push through until we do). My view is that we should ignore the forces pulling us every which way and set out to solve the issue that brought us here in the first place - the lack of quality + affordable CNCs for makers like us. So my answer to this question is that within 10 years, THE CNC COMPANY will be THE place makers go for resources, machines, and community - much like how HBO may be THE place for quality TV shows or Prusa may be THE place for reliable hobbyist 3D printers. I don't want to live in a deterministic world, I want to live in one in which I can decide the future. But hey, this is a lot of promises from a random guy who's yet to find any real success yet. We'll see where this actually goes.
